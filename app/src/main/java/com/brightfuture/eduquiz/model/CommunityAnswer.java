package com.brightfuture.eduquiz.model;

public class CommunityAnswer {
    private String ca_id, cq_id, ca_answer, u_id, u_name, ca_location, ca_created, ca_status;

    public String getCa_id() {
        return ca_id;
    }

    public void setCa_id(String ca_id) {
        this.ca_id = ca_id;
    }

    public String getCq_id() {
        return cq_id;
    }

    public void setCq_id(String cq_id) {
        this.cq_id = cq_id;
    }

    public String getCa_answer() {
        return ca_answer;
    }

    public void setCa_answer(String ca_answer) {
        this.ca_answer = ca_answer;
    }

    public String getU_id() {
        return u_id;
    }

    public void setU_id(String u_id) {
        this.u_id = u_id;
    }

    public String getU_name() {
        return u_name;
    }

    public void setU_name(String u_name) {
        this.u_name = u_name;
    }

    public String getCa_location() {
        return ca_location;
    }

    public void setCa_location(String ca_location) {
        this.ca_location = ca_location;
    }

    public String getCa_created() {
        return ca_created;
    }

    public void setCa_created(String ca_created) {
        this.ca_created = ca_created;
    }

    public String getCa_status() {
        return ca_status;
    }

    public void setCa_status(String ca_status) {
        this.ca_status = ca_status;
    }
}
